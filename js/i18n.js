// Switch language
function switchLanguage(lang) {
    if (lang === currentLanguage) return;

    currentLanguage = lang;
    localStorage.setItem('jaihoslots-language', lang);

    // Update button text
    const langText = document.querySelector('.lang-text');
    if (langText) {
        langText.textContent = lang === 'en' ? 'EN' : 'рд╣рд┐рдВ';
    }

    // Update active state
    document.querySelectorAll('.lang-option').forEach(option => {
        if (option.dataset.lang === lang) {
            option.classList.add('active');
        } else {
            option.classList.remove('active');
        }
    });

    // Apply translations
    applyTranslations(lang);

    // Update HTML lang attribute
    document.documentElement.setAttribute('lang', lang);
}

// Apply translations
function applyTranslations(lang) {
    const t = translations[lang];
    if (!t) return;

    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (t[key]) {
            // Handle input placeholders
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = t[key];
            } 
            // Handle button text (preserve icon)
            else if (element.tagName === 'BUTTON' || element.classList.contains('button')) {
                const icon = element.querySelector('i');
                if (icon) {
                    element.innerHTML = icon.outerHTML + ' ' + t[key];
                } else {
                    element.innerHTML = t[key];
                }
            }
            // Handle regular elements
            else {
                element.innerHTML = t[key];
            }
        }
    });
}

